SELECT * FROM CUSTOMERS WHERE LAST_NAME NOT LIKE '%ER'ORDER BY FIRST_NAME
SELECT * FROM CUSTOMERS WHERE LAST_NAME NOT LIKE 'T%S'ORDER BY FIRST_NAME 
